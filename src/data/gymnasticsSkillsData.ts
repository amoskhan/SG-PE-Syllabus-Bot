export const getGymnasticsChecklist = (skillName: string): string[] => {
    const normalizedSkill = skillName.trim().toLowerCase();
    const sections = GYMNASTICS_SKILLS_TEXT.split('SKILL:');

    const matchingSection = sections.find(section => {
        const lines = section.trim().split('\n');
        return lines[0].trim().toLowerCase() === normalizedSkill;
    });

    if (!matchingSection) return [];

    return matchingSection
        .split('\n')
        .map(line => line.trim())
        .filter(line => /^\d+\./.test(line));
};
